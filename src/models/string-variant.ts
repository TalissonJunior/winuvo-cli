export class StringVariant {
    /**
     * Initial state
     */
    original: string;
    /**
     * Uppercase
     * @example STRINGWITHUPPERCASE
     */
    upperCase: string;
    /**
     * Lowercase
     * @example stringwithlowercase
     */
    lowerCase: string;
    /**
     * Initial letter in uppercase and the others in lowercase
     * @example stringWithCamelCase
     */
    camelCase: string;
    /**
     * Has all the first letters in uppercase
     * @example StringWithCapitalize
     */
    capitalized: string;
    /**
     * String with dashes 
     * @example string-with-dash
     */
    dashed:string; 
}